-- 1. Create tables (idempotent)
-- ============================================

CREATE TABLE IF NOT EXISTS specialties (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL,
  icon text NOT NULL,
  sort_order int NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS doctors (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL,
  specialty_id uuid NOT NULL REFERENCES specialties(id),
  bio text NOT NULL DEFAULT '',
  image_url text NOT NULL DEFAULT ''
);

CREATE TABLE IF NOT EXISTS availability (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  doctor_id uuid NOT NULL REFERENCES doctors(id),
  date date NOT NULL,
  slots text[] NOT NULL DEFAULT '{}'
);

CREATE TABLE IF NOT EXISTS appointments (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  doctor_id uuid NOT NULL REFERENCES doctors(id),
  specialty_id uuid NOT NULL REFERENCES specialties(id),
  date date NOT NULL,
  time text NOT NULL,
  patient_name text NOT NULL DEFAULT '',
  created_at timestamptz NOT NULL DEFAULT now()
);

-- 2. Enable RLS but allow public read/write for hackathon
-- ============================================

ALTER TABLE specialties ENABLE ROW LEVEL SECURITY;
ALTER TABLE doctors ENABLE ROW LEVEL SECURITY;
ALTER TABLE availability ENABLE ROW LEVEL SECURITY;
ALTER TABLE appointments ENABLE ROW LEVEL SECURITY;

DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Public read specialties') THEN
    CREATE POLICY "Public read specialties" ON specialties FOR SELECT USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Public read doctors') THEN
    CREATE POLICY "Public read doctors" ON doctors FOR SELECT USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Public read availability') THEN
    CREATE POLICY "Public read availability" ON availability FOR SELECT USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Public all appointments') THEN
    CREATE POLICY "Public all appointments" ON appointments FOR ALL USING (true);
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Public update availability') THEN
    CREATE POLICY "Public update availability" ON availability FOR UPDATE USING (true);
  END IF;
END $$;

